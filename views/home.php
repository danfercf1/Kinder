    <div>FastDevelPHP es un sencillo framework orientado a programadores de PHP que necesitan  una guía sencilla, bien documentada y elegante para crear aplicaciones web bién estructuradas, seguras y de manera mas rápida.
        <p>Si eres un desarrollador que vive en el mundo real de las cuentas de  alojamiento  compartido y con clientes que te establecen plazos y estás cansado de  frameworks en donde tienes que escribir multitud de lineas de codigo  para algo sencillo, exponen la estructura y/o atributos de tu Base de  Datos, tienen documentacion compleja y totalmente indocumentada,  FastDevelPHP fué creado con motivos de cubrir esas y otras  complicaciones.</p>
        <p><label>FastDevelPHP</label> tiene las siguientes ventajas:</p>
        <ul>
            <li>Programacion Orientada a Objetos MVC.</li>
            <li>Rendiemiento Excepcional.</li>
            <li>Configuracion sencilla tanto en Localhost como tambien en los servidores de alojamiento.</li>
            <li>Cero uso de la consola de linea de Comando.</li>
            <li>Estructuración u organización de los archivos según su tipo o finalidad.</li>
            <li>Reducir la cantidad de líneas de código (lineas de instrucción).</li>
            <li>Consola de Test de funciones con acceso total a todas las funciones y variables del sitio.</li>
            <li>Biblioteca de funciones de uso frecuente.</li>
            <li>Seguridad de la estructura y/o atributos de la Base de Datos usando alias.</li>
            <li>Generar automáticamente los objetos modelo (ORM) de acuerdo a la estructura de la Base de Datos.</li>
            <li>Nombres de URL significativos.</li>
        </ul>
